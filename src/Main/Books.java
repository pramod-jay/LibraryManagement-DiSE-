    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            // TODO add your handling code here:
            // create connection to database
            Connection con = DB.database.getconnection();
            
            SimpleDateFormat sf = new SimpleDateFormat("yyyy-MM-dd");
            String dateprint = sf.format(dtePrint.getDate());
            String dateadded = sf.format(dteAdded.getDate());
            
            String meadium = "English";
            if (radSinhala.isSelected()){
                meadium = "Sinhala";
            }else if(radTamil.isSelected()){
                meadium = "Tamil";
            }else if(radOther.isSelected()){
                meadium = "Other";
            }
            // Create prepared statement with the sql query
            PreparedStatement st1 = null;
            try {
                st1 = con.prepareStatement("UPDATE `books` SET `name`='"+txtName.getText()+"', `isbn`='"+txtIsbn.getText()+"', `dateofprint`='"+dateprint+"', `author`='"+txtAuthor.getText()+"', `price`='"+txtPrice.getText()+"', `dateofadded`='"+dateadded+"', `burrowingprice`='"+txtBprice.getText()+"', `meadium`='"+meadium+"', `category`='"+cmbCategory.getSelectedItem()+"', `donatedby`='"+txtDonated.getText()+"' WHERE `bookid`='"+txtId.getText()+"';");
            } catch (SQLException ex) {
                Logger.getLogger(Books.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                // execute query
                st1.executeUpdate();
            } catch (SQLException ex) {
                Logger.getLogger(Books.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            // show success message
            JOptionPane.showMessageDialog(this, "Successfully Updated...");
        } catch (ClassNotFoundException | InstantiationException | IllegalAccessException ex) {
            Logger.getLogger(Books.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

