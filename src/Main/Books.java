    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        try (Connection con = DB.database.getconnection()) {
// TODO add your handling code here:
            // create connection to database
                        SimpleDateFormat sf = new SimpleDateFormat("yyyy-MM-dd");
            String dateprint = sf.format(dtePrint.getDate());
            String dateadded = sf.format(dteAdded.getDate());
            String meadium = "English";
            if (radSinhala.isSelected()){
                meadium = "Sinhala";
            }else if(radTamil.isSelected()){
                meadium = "Tamil";
            }else if(radOther.isSelected()){
                meadium = "Other";
            }   // Create prepared statement with the sql query
            PreparedStatement st1 = con.prepareStatement("INSERT INTO `books` (`bookid`, `name`, `isbn`, `dateofprint`, `author`, `price`, `dateofadded`, `burrowingprice`, `meadium`, `category`, `donatedby`) VALUES ('"+txtId.getText()+"', '"+txtName.getText()+"', '"+txtIsbn.getText()+"', '"+dateprint+"', '"+txtAuthor.getText()+"', '"+txtPrice.getText()+"', '"+dateadded+"', '"+txtBprice.getText()+"', '"+meadium+"', '"+cmbCategory.getSelectedItem()+"', '"+txtDonated.getText()+"');");
            // execute query
            st1.executeUpdate();
            // show success message
            JOptionPane.showMessageDialog(this, "Successfully Inserted...");
        } catch (ClassNotFoundException | InstantiationException | IllegalAccessException | SQLException ex) {
            Logger.getLogger(Books.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            // TODO add your handling code here:
            Connection con = DB.database.getconnection();
            String sql = "SELECT bookid,name,isbn,dateofprint,author,price,dateofadded,burrowingprice,meadium,category,donatedby FROM lms.books WHERE bookid='"+txtId.getText()+"';";
            PreparedStatement st = null;
            try {
                st = con.prepareStatement(sql);
            } catch (SQLException ex) {
                Logger.getLogger(Books.class.getName()).log(Level.SEVERE, null, ex);
            }
            ResultSet rs = null;
            try {
                rs = st.executeQuery();
            } catch (SQLException ex) {
                Logger.getLogger(Books.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                while (rs.next()) {
                    txtName.setText(rs.getString("name"));
                    txtIsbn.setText(rs.getString("isbn"));
                    dtePrint.setDate(rs.getDate("dateofprint"));
                    txtAuthor.setText(rs.getString("author"));
                    txtPrice.setText(rs.getString("price"));
                    dteAdded.setDate(rs.getDate("dateofadded"));
                    txtBprice.setText(rs.getString("burrowingprice"));
                    txtDonated.setText(rs.getString("donatedby"));
                    
                    String meadium = rs.getString("meadium");
                    if(meadium.equalsIgnoreCase("English")){
                        radEnglish.setSelected(true);
                    }else if(meadium.equalsIgnoreCase("Sinhala")){
                        radSinhala.setSelected(true);
                    }else if(meadium.equalsIgnoreCase("Tamil")){
                        radTamil.setSelected(true);
                    }else if(meadium.equalsIgnoreCase("Other")){
                        radOther.setSelected(true);
                    }
                    
                    cmbCategory.setSelectedItem(rs.getString("category"));
                }
            } catch (SQLException ex) {
                Logger.getLogger(Books.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (ClassNotFoundException | InstantiationException | IllegalAccessException ex) {
            Logger.getLogger(Books.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

